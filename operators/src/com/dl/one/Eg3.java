package com.dl.one;

public class Eg3 {
	//Unary Operators

	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		System.out.println(a++);//10
		System.out.println(a);//post increment value by 1
		System.out.println(++a);//pre-increment value by 1
		
		System.out.println(b++);
		System.out.println(b);
		System.out.println(++b);
		System.out.println(a-- + --a);
 
			
		}
		

	}


