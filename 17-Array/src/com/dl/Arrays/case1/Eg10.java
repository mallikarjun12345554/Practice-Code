
package com.dl.Arrays.case1;

public class Eg10 {
	public static void main(String[] args) {
		int a[] = {10, 20, 30, 40, 50};
		int i = 0;
		while (i<5) {
			System.out.println(a[i]);
			i++;
		}
		System.out.println("    ");
		int j = 4;
		while(j>=0) {
			System.out.println(a[j]);
			j--;
		}
	}

}
