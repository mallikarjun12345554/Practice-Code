package com.dl.Two;

public class UserProfile {
	String firstName;
	String lastName;
	String job;
	String address;
	long contact;
	public UserProfile(String firstName, String lastName, String job, String address, long contact) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.job = job;
		this.address = address;
		this.contact = contact;
	}

	
}
